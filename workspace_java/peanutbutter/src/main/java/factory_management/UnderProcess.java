package factory_management;

public class UnderProcess {
    private String processCode;
    private String underProcessCode;
    private String processName;
    private String image;
    private String productContent;

    // Getters and Setters
    public String getProcessCode() { return processCode; }
    public void setProcessCode(String processCode) { this.processCode = processCode; }
    public String getUnderProcessCode() { return underProcessCode; }
    public void setUnderProcessCode(String underProcessCode) { this.underProcessCode = underProcessCode; }
    public String getProcessName() { return processName; }
    public void setProcessName(String processName) { this.processName = processName; }
    public String getImage() { return image; }
    public void setImage(String image) { this.image = image; }
    public String getProductContent() { return productContent; }
    public void setProductContent(String productContent) { this.productContent = productContent; }
}